package com.fred.projetocorona;

public class Testes {
    private long id;
    private String data_teste;
    private String data_resultado;
    private String resultado;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getData_teste() {
        return data_teste;
    }

    public void setData_teste(String data_teste) {
        this.data_teste = data_teste;
    }

    public String getData_resultado() {
        return data_resultado;
    }

    public void setData_resultado(String data_resultado) {
        this.data_resultado = data_resultado;
    }

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }
}
